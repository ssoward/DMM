package com.soward.object;

public class InstrumentInventory {
	String pid ="";
	String location ="";
	String instrPid ="";
	String count ="";


	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getInstrPid() {
		return instrPid;
	}
	public void setInstrPid(String instrPid) {
		this.instrPid = instrPid;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}



}
